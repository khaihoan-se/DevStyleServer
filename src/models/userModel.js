const mongoose = require('mongoose')


const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: [true, "Please enter your name!"],
        trim: true
    },
    email: {
        type: String,
        required: [true, "Please enter your email!"],
        trim: true,
        unique: true
    },
    password: {
        type: String,
        required: [true, "Please enter your password!"]
    },
    role: {
        type: Number,
        default: 0 // 0 = user, 1 = admin
    },
    avatar: {
        type: String,
        default: "https://res.cloudinary.com/figdom/image/upload/v1654455908/DevStyle/120129515_176217763987416_7070652172320693443_n_z3tcz3.jpg"
    }
}, {
    timestamps: true
})

module.exports = mongoose.model("Users", userSchema)